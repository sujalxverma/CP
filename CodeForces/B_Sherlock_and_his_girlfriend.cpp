vector<ll> sieveOfEratosthenes(ll n) {
    vector<ll> numbers(n + 1, 1);  // 0 = prime, 1 = not prime

    for (ll i = 2; i * i <= n; i++) {
        if (numbers[i]==2) continue;

        for (ll j = i * i; j <= n; j += i) {
            numbers[j] = 2;  // mark as not prime
        }
    }


    return numbers;
}


