/*
    1. first we have two conditions : a[i]+a[j] <= a[k] and a[i] + a[j] + a[k] > a[n]
    2. first we find the range for 1st condition, lets say for eg, it is 5 to 13
    3. then we find range for second condition in b/w, 5 to 13, eg, now range is 10 to 13, hence for this range,
       both conditions are fullfilled.
*/

void solve() {
    int n;
    cin >> n;
    vector<int> a(n);
    for (auto &x : a) cin >> x;
    sort(a.begin(), a.end());

    ll ans = 0;
    for (int i = 0; i < n - 2; i++) {
        for (int j = i + 1; j < n - 1; j++) {
            ll sum = a[i] + a[j];

            // a[k] must be < sum (triangle condition)
            auto low = lower_bound(a.begin() + j + 1, a.end(), sum);
            int len = low - (a.begin() + j + 1);
            if (len == 0) continue;

            // a[i] + a[j] + a[k] > a[n-1]
            // => a[k] > a[n-1] - a[i] - a[j]
            auto high = upper_bound(a.begin() + j + 1, a.begin() + j + 1 + len, a[n - 1] - sum);
            ans += (a.begin() + j + 1 + len) - high;
        }
    }

    cout << ans << "\n";
}
