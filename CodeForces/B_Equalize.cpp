/*
    1. Sort the array so that the elements are in increasing order.
    2. Remove duplicates from the array because only unique elements matter (elements that are already equal canâ€™t become more equal by adding a permutation).
    3. Use two pointers (a sliding window) to find the longest continuous subarray where the difference between the maximum and minimum element is at most n - 1.
    4. Move the left pointer forward whenever the difference between the current right pointer element and the left pointer element becomes greater than n - 1.
    5. Move the right pointer forward to expand the window.
    6. The length of this longest subarray is the maximum number of equal elements you can get after adding some permutation.
*/


void solve() {
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    sort(a.begin(), a.end());
    a.resize(unique(a.begin(), a.end()) - a.begin());
    int pnt = 0, ans = 0;
    for (int i = 0; i < a.size(); i++) {
        while(a[i] - a[pnt] >= n) {
            pnt++;
        }
        ans = max(ans, i - pnt + 1);
    }
    cout << ans << endl;
}


